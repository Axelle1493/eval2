package entities;

public class Zone {
    private int idZone;
    private String nomZone;
    public Zone() {
    }
    public int getIdZone() {
        return idZone;
    }
    public void setIdZone(int idZone) {
        this.idZone = idZone;
    }
    public String getNomZone() {
        return nomZone;
    }
    public void setNomZone(String nomZone) {
        this.nomZone = nomZone;
    }
}
